/**
 * Basic output functions:
 */
int _putchar(char c);  // Writes a single character to stdout
void _puts_recursion(char *s);  // Prints a string recursively, followed by a newline
void _print_rev_recursion(char *s);  // Prints a string in reverse order recursively

/**
 * String manipulation functions:
 */
int _strlen_recursion(char *s);  // Calculates the length of a string recursively
int is_palindrome(char *s);  // Checks if a string is a palindrome
int wildcmp(char *s1, char *s2);  // Compares two strings with wildcard characters

/**
 * Mathematical functions:
 */
int factorial(int n);  // Calculates the factorial of a non-negative integer
int _pow_recursion(int x, int y);  // Calculates x raised to the power of y recursively
int _sqrt_recursion(int n);  // Calculates the non-negative square root of n recursively

/**
 * Number-related functions:
 */
int is_prime_number(int n);  // Determines if a number is prime

